       `LINEGROSSRATE`, `LINECOST`, `QUANTITY`, `QUANTITYBYUNITS`,
       `LINECOSTRATE`, `VALUEATCOSTRATE`, `VALUEATPURCHASERATE`, `VALUEATRETAILRATE` 
FROM billitemfinancedetails 
WHERE `ID` < 3314958
  AND `ID` > 3312249
ORDER BY id DESC;
