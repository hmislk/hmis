-- Check bill items created on 9th July 2025
SELECT id, `CREATEDAT`, `BILLITEMFINANCEDETAILS_ID`, `NETRATE`, `RATE`, `QTY`, `NETVALUE`, `GROSSVALUE` 
FROM billitem 
WHERE `BILL_ID` = 3312022
ORDER BY id DESC;

